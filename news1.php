            <h3>Mengenal Permainan Tradisional Jaranan</h3>
            <p>Seperti yang sudah disinggung pada bagian sebelumnya, jaranan merupakan permainan tradisional yang berasal dari masyarakat Jawa. Dikutip dari buku 45 Permainan Tradisional Anak Indonesia, penamaan permainan tradisional ini berasal dari asal kata 'jaran' dalam bahasa Jawa.</p>
            <p>Dalam bahasa Jawa, kata 'jaran' ini memiliki arti sebagai kuda. Penamaan 'jaranan' ini merujuk kepada kuda-kudaan yang digunakan oleh setiap pemain ketika memainkan permainan tradisional tersebut. Setiap pemain akan bermain seolah-olah menunggangi kuda ketika memainkan permainan tersebut. Biasanya setiap pemain akan bermain secara berkelompok bersama teman-teman lainnya.</p>
