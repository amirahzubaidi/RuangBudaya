            <h3>Alat Bermain</h3>
            <p>Sebelum memainkan permainan tradisional ini, para pemain mesti mempersiapkan jaranan yang akan digunakan dalam permainan tersebut. Jika persiapan ini bisa dilakukan dalam waktu lama, setiap pemain bisa membuat jaranan dari anyaman bambu. Anyaman ini dibentuk seperti kuda-kudaan yang akan dinaiki nantinya. Meskipun demikian, dibutuhkan keahlian khusus agar jaranan yang dibuat dari anyaman bambu ini bisa memiliki bentuk seperti yang diinginkan.</p>
            <p>Jika pemain tidak memiliki waktu luang dan ingin memainkan permainan ini secepatnya, maka bisa membuat jaranan dari batang dan pelepah pisang. Para pemain bisa membentuk batang pisang ini seperti kuda-kudaan. Selain itu, para pemain juga bisa membuat tali dari serat batang pisang yang nantinya bisa dijadikan pengait untuk kuda-kudaan tersebut. Jangan lupa pula mempersiapkan lidi atau sejenisnya yang akan digunakan sebagai pelecut dalam permainan tradisional ini nantinya.</p>
