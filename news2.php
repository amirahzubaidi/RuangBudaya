            <small>Kehadiran dan ketulusan kerja mereka adalah karya bakti yang tidak ternilai harganya bagi generasi hari ini dan mendatang, agar mereka tidak kehilangan karakter dan jati diri dalam putaran zaman dan derasnya arus modernisasi.</small>
            <p>Panggung Maestro adalah panggung penghargaan bagi para maestro yang telah mendedikasikan hidup mereka dalam menjaga dan merawat kesenian tradisional, sehingga budaya bangsa kita lestari hingga kini. Kehadiran dan ketulusan kerja mereka adalah karya bakti yang tidak ternilai harganya bagi generasi hari ini dan mendatang, agar mereka tidak kehilangan karakter dan jati diri dalam putaran zaman dan derasnya arus modernisasi.</p>
            <p>Penampilan para maestro diharapkan akan meningkatkan apresiasi, menumbuhkan kepedulian, dan memantik daya kreatif dalam upaya pemeliharaan dan pengembangan seni dan budaya.</p>
            <p>Panggung Maestro kelima, yang dipersembahkan oleh Yayasan Bali Purnati bekerja sama dengan Direktorat Perfilman, Musik dan Media, Direktorat Jenderal Kebudayaan, Kementerian Pendidikan, Kebudayaan, Riset dan Teknologi Republik Indonesia, dan didukung oleh Yayasan Taut Seni & Bumi Purnati Indonesia; diselenggarakan pada 13-14 Juni 2024 di Teater Wahyu Sihombing, Taman Ismail Marzuki, Jakarta. Panggung Maestro kali ini menghadirkan maestro kesenian dari dua daerah provinsi: Jawa Timur dan Nusa Tenggara Barat.</p>
            <p>Panggung Maestro adalah sebuah pernyataan (bukan pengukuhan) penghormatan kepada para seniman yang telah mengalirkan energi seni-budaya yang didapat dari para pendahulu mereka kepada kita generasi penerus. Energi adalah daya hidup, semacam sukma, yang tidak akan mati. "Tapi sukma hanya ada jika raga terjaga. Kami berniat, berjanji, untuk menjadi pewaris aktif dengan memelihara dan memupuk energi itu, hingga akan lahir buah dan biji yang mendorong pertumbuhan budaya seterusnya,"kata Endo Suanda, Dewan Artistik Panggung Maestro.</p>
            <p>Sejak diadakannya Panggung Maestro pertama kali pada Juli 2023, sudah ditampilkan 25 orang Maestro dan 250 orang pendukung pertunjukan. Telah ditonton oleh 2.470 orang dengan lebih dari 25.000.000 tampilan digital, Panggung Maestro terus merayakan kekayaan dan keragaman kesenian Indonesia yang dapat memperkuat kearifan sosial, ketahanan martabat, dan pertumbuhan sosial-ekonomi.</p>
            <p>"Satu hal yang sangat membahagiakan sekaligus mengharukan, manakala di dalam Panggung Maestro kali ini kita mendapat kesempatan bertemu dengan para penari dan penggubah tari yang berusia di atas 70 tahun, bahkan ada yang sudah melebihi 90 tahun, tetapi masih tetap berkarya, jelasnya. "Lama rentang waktu yang mereka jalani dalam berkarya bukan main-main. Konsep wiraga, wirama, serta wirasa sudah jauh mereka lampaui dan yang mampu ada dan selalu ada adalah "kasunyatan", yang senantiasa bersemayam di dalam tubuh mereka. Itulah sejatinya sang Maestro," ujar Sulistyo Tirtokusumo, Dewan Artistik Panggung Maestro.</p>
            <p>Selain Panggung Maestro, juga diadakan Panggung Wacanayaitu forum gelar wicara untuk mengartikulasikan nilai-nilai yang melingkari dan menyelimuti kerja keempuan. Panggung Wacana menjadi kesempatan penting bagi para peserta, seniman, budayawan, guru, pelajar/mahasiswa dan siapa saja yang tertarik dan peduli pada seni tradisi, juga untuk mendengarkan dan berbincang-bincang dengan para tokoh kebudayaan Indonesia yang jarang kita temui.</p>
            
